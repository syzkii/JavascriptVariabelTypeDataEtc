{
    let x = 10;
    let y = 5

    x += y; // artinya -> x = x + y;
    x -= y; // artinya -> x = x - y;
    x *= y; // artinya -> x = x * y;
    x /= y; // artinya -> x = x / y;
    x %= y; // artinya -> x = x % y;
}